export const calculateSizes = (isSmall, isMobile, isTablet) => {
  return {
    deskScale: isSmall ? 0.045 : isMobile ? 0.055 : 0.06,
    deskPosition: isSmall
      ? [0.4, -4.2, 0]
      : isMobile
      ? [0.5, -4.8, 0]
      : isTablet
      ? [0.3, -6.2, 0]
      : [0.25, -6.5, 0],

    cubePosition: isSmall
      ? [0.8, -7.5, -9]
      : isMobile
      ? [4.5, -4.8, 0]
      : isTablet
      ? [4.8, -5.2, 0]
      : [13.5, -8, 0],

    reactLogoPosition: isSmall
      ? [2.5, 3, -14]
      : isMobile
      ? [4.8, 4.5, -3]
      : isTablet
      ? [5, 4, 0]
      : [13.5, 1, 0],

    ringPosition: isSmall
      ? [-18, 12, -10]
      : isMobile
      ? [-15, 14, 0]
      : isTablet
      ? [-22, 11, 0]
      : [-40, 8, 0], // Adjusted for better visibility

    targetPosition: isSmall
      ? [-2.8, -7.5, -7.5]
      : isMobile
      ? [-6.5, -7, -9.5]
      : isTablet
      ? [-10.5, -7, -9.5]
      : [-18, -12.5, -9.5],
  };
};

